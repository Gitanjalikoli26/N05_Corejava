package org.tnsif.wrapperclassdemo;
/* Program to demonstrate on autoboxing
 * AutoBoxing:conversion of primitive type to object type*/
public class AutoBoxing {

	public static void main(String[] args) {
		char c='A';
		//converting to data type into object type
		Character ch=c;
		System.out.println(ch);
		
		

	}

}
